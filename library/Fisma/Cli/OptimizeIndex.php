class Fisma_Cli_OptimizeIndex extends Fisma_Cli_Abstract
{
    /**
     * Run the search engine's optimize routine
     */
    protected function _run()
    {     
        $searchEngine = Zend_Registry::get('search_engine');
        
        $searchEngine->optimizeIndex();
    }
}