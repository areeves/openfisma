/**
 * An interface for implementing a Scan Result injection plugin.
 *
 * @package   Controller
 * @copyright (c) Endeavor Systems, Inc. 2008 (http://www.endeavorsystems.com)
 * @license   http://www.openfisma.org/mw/index.php?title=License
 */
interface ScanResult
{
    /** 
     * To decide wether or not the file can be parsed
     *
     * @return boolean
     */
    public function isValid($file);
    
    /** 
     * Convert the file to an iteratable dataset which can be directly injected
     * into the database.
     *
     * @return mixed
     *
     * @todo The return type can be specified with higher precision than this.
     * Currently, the interface is not documented well.
     */
    public function parse($file);
}
