 * @package    Test
 * @subpackage Test_Harness
 */
class Test_Harness_Database extends Test_Harness_Abstract
{
    /**
     * Match all subclasses of FismaUnitTest.
     * 
     * @param string $className
     * @return bool
     */
    protected function _includeClassInSuite($className)
    {
        return is_subclass_of($className, 'Test_Case_Database');
    }
}
